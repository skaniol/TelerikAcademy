// 08. Create a console application that calculates and prints
//     the square of the number 12345.

using System;

class E08_SquareOf12345
{
    static void Main()
    {
        Console.WriteLine("The square of 12345 is " + 12345 * 12345);
        Console.WriteLine("The square root of 12345 is " + Math.Sqrt(12345));
    }
}